/**
 * Get Integer value from json data;
 * @param value
 * @return
 */
public static int getIntValue(String value){
	
	try{
	
		return Integer.parseInt(value);
	}catch(NumberFormatException ex){
		ex.printStackTrace();
		return 0;
	}
	
	
}


	public static String getOrderQtyBySKU(String enterOrderQty, String sku){

		if(enterOrderQty==null || enterOrderQty.isEmpty())
			return null;

		int currentSku=getIntValue(sku);
		if(currentSku==0)
			return null;

		int orderQty;
		int currentOrderQty=getIntValue(enterOrderQty);


		try{

			orderQty = currentOrderQty - (currentOrderQty % currentSku);
			return String.valueOf(orderQty);

		}catch(Exception ex){
			ex.printStackTrace();
			return "";
		}


	}


